namespace Threads.Marker.Commands.SubstitutionProperties {
    public enum FlagProperty {
        /// <summary>
        /// Boolean values will be displayed as "true" for true and "false" for false.
        /// </summary>
        TrueFalse,

        /// <summary>
        /// Boolean values will be displayed as "yes" for true and "no" for false.
        /// </summary>
        YesNo,

        /// <summary>
        /// Boolean values will be displayed as 1 for true and 0 for false.
        /// </summary>
        OneZero
    }
}
